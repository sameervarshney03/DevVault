async function fetchRepositoriesForCurrentUser(req, res) {
  const { userID } = req.params;

  if (!mongoose.Types.ObjectId.isValid(userID)) {
    return res.status(400).json({ error: "Invalid User ID!" });
  }

  try {
    const repositories = await Repository.find({ owner: userID });
    res.json({ message: "Repositories found!", repositories: repositories || [] });
  } catch (err) {
    console.error("Error during fetching user repositories: ", err.message);
    res.status(500).json({ error: "Server error" });
  }
}

async function updateRepositoryById(req, res) {
  const { id } = req.params;
  const { content, description } = req.body;

  try {
    const repository = await Repository.findById(id);
    if (!repository) {
      return res.status(404).json({ error: "Repository not found!" });
    }

    repository.content.push(content);
    repository.description = description;

    const updatedRepository = await repository.save();

    res.json({
      message: "Repository updated successfully!",
      repository: updatedRepository,
    });
  } catch (err) {
    console.error("Error during updating repository : ", err.message);
    res.status(500).send("Server error");
  }
}

async function toggleVisibilityById(req, res) {
  const { id } = req.params;

  try {
    const repository = await Repository.findById(id);
    if (!repository) {
      return res.status(404).json({ error: "Repository not found!" });
    }

    repository.visibility = !repository.visibility;

    const updatedRepository = await repository.save();

    res.json({
      message: "Repository visibility toggled successfully!",
      repository: updatedRepository,
    });
  } catch (err) {
    console.error("Error during toggling visibility : ", err.message);
    res.status(500).send("Server error");
  }
}

async function deleteRepositoryById(req, res) {
  const { id } = req.params;
  try {
    const repository = await Repository.findByIdAndDelete(id);
    if (!repository) {
      return res.status(404).json({ error: "Repository not found!" });
    }

    res.json({ message: "Repository deleted successfully!" });
  } catch (err) {
    console.error("Error during deleting repository : ", err.message);
    res.status(500).send("Server error");
  }
}

async function toggleStar(req, res) {
  const { id } = req.params;
  const { currentUserId } = req.body;
  try {
    const user = await User.findById(currentUserId);
    if (!user) {
      return res.status(404).json({ error: "User not found!" });
    }
    const index = user.starRepos.indexOf(id);
    if (index > -1) {
      user.starRepos.splice(index, 1);
    } else {
      user.starRepos.push(id);
    }
    await user.save();
    res.status(200).json({ message: "Star toggled successfully" });
  } catch (err) {
    console.error("Error toggling star:", err.message);
    res.status(500).send("Server error");
  }
}

